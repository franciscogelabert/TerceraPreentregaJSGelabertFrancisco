class Bebida {
    nombre;
    precio;
    cantidad;
    calorias;
    sodio;
    alcohol;
    
    constructor (nombre, precio, cantidad, calorias, sodio, alcohol)
    
    {
    this.nombre=nombre;
    this.precio=precio;
    this.cantidad=cantidad;
    this.calorias=calorias;
    this.sodio=sodio;
    this.alcohol=alcohol;
    }
        
}
    